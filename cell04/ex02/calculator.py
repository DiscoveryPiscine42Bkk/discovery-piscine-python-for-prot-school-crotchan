# รับค่าจากผู้ใช้
num1 = float(input("Give me the first number: "))  # รับตัวเลขแรก
num2 = float(input("Give me the second number: "))  # รับตัวเลขที่สอง

print("Thank you!")

# คำนวณและแสดงผลลัพธ์
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {num1 / num2}")
print(f"{num1} * {num2} = {num1 * num2}")
