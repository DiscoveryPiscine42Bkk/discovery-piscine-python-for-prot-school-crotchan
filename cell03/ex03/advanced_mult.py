i = 0  # เริ่มจากตัวเลข 0

while i <= 10:  # ลูปแรก เพื่อทำงานตั้งแต่ 0 ถึง 10
    j = 0  # เริ่มจากตัวเลข 0 สำหรับการคูณ
    print(f"Table de {i}:", end=" ")  # พิมพ์หัวข้อของตาราง
    while j <= 10:  # ลูปที่สอง เพื่อคูณ i กับ j
        print(i * j, end=" ")  # คูณ i กับ j แล้วแสดงผล
        j += 1  # เพิ่มค่า j ทีละ 1
    print()  # เพิ่มบรรทัดใหม่หลังจากแสดงผลตารางเสร็จ
    i += 1  # เพิ่มค่า i ทีละ 1 เพื่อไปแสดงตารางถัดไป
